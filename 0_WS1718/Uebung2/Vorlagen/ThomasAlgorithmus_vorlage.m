function [x]=ThomasAlgorithmus(a,b,c,d)
% a: Unter-Diagonalelemente
% b: Diagonalelemente
% c: Ober-Diagonalelemente
%
%
%       b1 c1  0  0  0 ...
%  T =  a2 b2 c2  0  0 ...
%        0 a3 b3 c3  0 ...
%        . . . . . . . . .
%
n = length(a);
x = zeros(n,1);

c(1) = . . . .
d(1) = . . . .
for i = 2:n
    c(i) = . . . . 
    d(i) = . . . .
end

x(n) = d(n);
for i= n-1:-1:1
    x(i) = . . . .
end
